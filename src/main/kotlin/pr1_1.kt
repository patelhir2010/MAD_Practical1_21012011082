fun main() {
    val a = 5               // Int
    val b = 5.9      // Double
    val c = 'D'          // Char
    val d = "hello"        // Boolean
    val e = true
    val f = 96.35
    val g = 783214556
    val h = -5
    val i = 127
    println("Integer value: "+a)
    println("Float Value: "+b)
    println("Character value: "+c)
    println("String value: "+d)
    println("Boolean value: "+e)
    println("Double value: "+f)
    println("Long value: "+g)
    println("Short value: "+h)
    println("Byte value: "+i)
}