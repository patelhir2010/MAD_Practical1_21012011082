fun main(args: Array<String>) {

    // Integer to Double
    val Int = 10
    val Double = Int.toDouble()
    println("Integer value:" + Int)
    println("Double value (From Integer):" + Double)

    val String = 10
    val int = String.toInt()
    println("String value: "+String)
    println("Integer value (From String):"+int)

    val str = 78
    val double = str.toDouble()
    println("String value: "+str)
    println("Double value (From String: "+double)

}

